function mostrar(){
    var oct = document.getElementById("obj");
    oct.style.display ='block';
}
function ocultar(){
    var oct = document.getElementById("obj");
    oct.style.display='none';
}
var mos = document.getElementById("objetivo");
mos.onmouseover = mostrar;
mos.onmouseout = ocultar;

